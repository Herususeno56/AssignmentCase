function arrAdder(input) {
  let kosong = "";

  for (let i = 0; i < input[0].length; i++) {
    for (let j = 0; j < input.length; j++) {
      kosong += input[j][i];
    }
    kosong += " ";
  }
  console.log(kosong);
}

